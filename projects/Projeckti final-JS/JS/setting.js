function ifWantSideBar() {
  let checked = document.getElementById("check");

  if (checked.checked) {
    let sidebar = document.getElementById("sidebar");
    sidebar.style.backgroundImage = "url(images/sidebar-2.jpg)";
  } else {
    let sidebar = document.getElementById("sidebar");
    sidebar.style.backgroundImage = "none";
  }
}
checkbox.addEventListener("change", ifWantSideBar);

